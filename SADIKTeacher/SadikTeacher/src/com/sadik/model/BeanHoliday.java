package com.sadik.model;

public class BeanHoliday {
	
	int holiday_id;

	String holiday_name, holiday_date, schoolPkey;

	public String getSchoolPkey() {
		return schoolPkey;
	}

	public void setSchoolPkey(String schoolPkey) {
		this.schoolPkey = schoolPkey;
	}

	public int getHoliday_id() {
		return holiday_id;
	}

	public void setHoliday_id(int holiday_id) {
		this.holiday_id = holiday_id;
	}

	public String getHoliday_name() {
		return holiday_name;
	}

	public void setHoliday_name(String holiday_name) {
		this.holiday_name = holiday_name;
	}

	public String getHoliday_date() {
		return holiday_date;
	}

	public void setHoliday_date(String holiday_date) {
		this.holiday_date = holiday_date;
	}




}
